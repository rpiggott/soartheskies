<?php

namespace About\Service;

use Application\Entity\PageAbout;
use Doctrine\ORM\EntityManager;

class PageAboutRepository implements PageAboutRepositoryInterface
{
    /** @var EntityManager */
    private $entityManager;

    public function __construct(EntityManager $entityManager)
    {
        $this->entityManager = $entityManager;
    }

    public function findLatestEntry(): PageAbout
    {
        $repository = $this->entityManager->getRepository(PageAbout::class);
        $qb = $repository->createQueryBuilder('p');
        $qb->where('p.effectiveDate <= :now')
            ->orderBy('p.effectiveDate', 'DESC')
            ->setMaxResults( 1 );

        $qb->setParameter('now', new \DateTime());

        return $qb->getQuery()->getSingleResult();
    }
}

